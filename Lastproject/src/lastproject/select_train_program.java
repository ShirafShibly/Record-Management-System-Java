    private void btn_delete_completed_trainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delete_completed_trainActionPerformed
        int choice=JOptionPane.showConfirmDialog(null,"Are you sure you want to delete selected programme from completed list  ","Delete completed programme",JOptionPane.YES_NO_OPTION);  
        if(choice==0){
        con = mysqlconnect.connectDB();
        int row = tbl_select_train_completed.getSelectedRow();
       String table_click = (tbl_select_train_completed.getModel().getValueAt(row,0).toString());
       String s_nic = txt_select_train_nic.getText();
       try{
           String sql = "delete  from participation  where p_id = '"+table_click+"'and s_nic ='"+s_nic+"'" ;
           pst = con.prepareStatement(sql);
           pst.execute();
           update_completed_table();
           fill_src_table();
           
           
                    
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
       finally{
           closeall();
       }
        }
    }//GEN-LAST:event_btn_delete_completed_trainActionPerformed

