    private void btn_insert_completed_trainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insert_completed_trainActionPerformed
con = mysqlconnect.connectDB();
try{
    int row_count = tbl_select_train_completed.getRowCount();
    String query = "insert ignore into participation (s_nic,p_id,start_date,end_date) values (?,?,?,?)";
    pst = con.prepareStatement(query);
    
    
    
    
    
    for(int r = 0;r<row_count;r++){
        String s_nic = txt_select_train_nic.getText();
    String p_id = (String)tbl_select_train_completed.getValueAt(r, 0);
    String  start_date = (String)(tbl_select_train_completed.getValueAt(r, 2));
    String   end_date = (String)(tbl_select_train_completed.getValueAt(r, 3));
       
    pst.setString(1, s_nic);
    pst.setString(2, p_id);
    pst.setString(3, start_date );
    pst.setString(4, end_date  );
    

    pst.addBatch();
    
    }
    pst.executeBatch();
    JOptionPane.showMessageDialog(null,"Details added");
//    update_completed_table();
    
    
    
}catch(Exception e){
    JOptionPane.showMessageDialog(null, e);
}
finally{
    closeall();
}

    }//GEN-LAST:event_btn_insert_completed_trainActionPerformed

