    private void btn_edit_course_list_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_edit_course_list_deleteActionPerformed
        int choice=JOptionPane.showConfirmDialog(null,"Are you sure you want to delete this course","Delete course",JOptionPane.YES_NO_OPTION);
if(choice==0){
        con = mysqlconnect.connectDB();
        try{
            String sql = "delete from course_list where course_name=?";
            pst = con.prepareStatement(sql);
            pst.setString(1,cmb_edit_course_list_course_list.getSelectedItem().toString());
            
       
            pst.execute();
            JOptionPane.showMessageDialog(null,"Course  Deleted");
           
            btn_edit_course_list_refresh.doClick();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);

        }finally{
            closeall();
        }
}   
    }//GEN-LAST:event_btn_edit_course_list_deleteActionPerformed

