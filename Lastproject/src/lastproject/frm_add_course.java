    private void btn_edit_course_list_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_edit_course_list_addActionPerformed
        if(txt_edit_course_name.getText().isEmpty()==true){
        JOptionPane.showMessageDialog(null, "You have not entered the course name");
        txt_edit_course_name.requestFocusInWindow();
        }else{
        con = mysqlconnect.connectDB();
        try{
            String sql = "Insert into course_list (course_name) values(?)";
            pst = con.prepareStatement(sql);
            pst.setString(1,txt_edit_course_name.getText());
           
            
            pst.execute();
            JOptionPane.showMessageDialog(null,"Course added");
            
            cmb_edit_course_list_course_list.setSelectedIndex(0);
            txt_edit_course_name.setText("");
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);

        }finally{
            closeall();
        }
        add_course_list();
        txt_edit_course_name.requestFocusInWindow();
        
   
        
        
        }
    }//GEN-LAST:event_btn_edit_course_list_addActionPerformed

